GROQ_API_URL = "https://api.groq.com/openai/v1/chat/completions"


def _make_serializable(obj):
    """Recursively convert numpy/pandas types to native Python for JSON serialization."""
    if isinstance(obj, dict):
        return {k: _make_serializable(v) for k, v in obj.items()}
    elif isinstance(obj, list):
        return [_make_serializable(i) for i in obj]
    elif isinstance(obj, np.integer):
        return int(obj)
    elif isinstance(obj, np.floating):
        return round(float(obj), 2)
    elif isinstance(obj, np.ndarray):
        return obj.tolist()
    else:
        return obj


def call_groq(prompt: str, api_key: str, model: str = GROQ_MODEL, temperature: float = 0.3) -> str:
    """Call Groq API and return response text."""
    if not api_key or api_key == "gsk_your_groq_api_key_here":
        return _fallback_report(prompt)

    headers = {
        "Authorization": f"Bearer {api_key}",
        "Content-Type": "application/json"
    }
    payload = {
        "model": model,
        "messages": [
            {
                "role": "system",
                "content": (
                    "You are SmartReport AI, an enterprise business intelligence agent. "
                    "You generate concise, professional executive-level business reports. "
                    "Use clear language, highlight risks boldly, and always end with 3 actionable recommendations. "
                    "Format responses in clean sections with headers. Be data-driven and specific."
                )
            },
            {"role": "user", "content": prompt}
        ],
        "temperature": temperature,
        "max_tokens": 1200
    }
    try:
        response = requests.post(GROQ_API_URL, headers=headers, json=payload, timeout=30)
        response.raise_for_status()
        return response.json()["choices"][0]["message"]["content"]
    except requests.exceptions.ConnectionError:
        return _fallback_report(prompt)
    except requests.exceptions.HTTPError as e:
        status_code = e.response.status_code if e.response is not None else "unknown"
        print(f"Groq API HTTP error ({status_code}) using model '{model}': {e}")
        return _fallback_report(prompt)
    except (KeyError, IndexError, ValueError) as e:
        print(f"Unexpected Groq API response shape: {e}")
        return _fallback_report(prompt)
    except requests.exceptions.RequestException as e:
        print(f"Groq API request failed: {e}")
        return _fallback_report(prompt)


