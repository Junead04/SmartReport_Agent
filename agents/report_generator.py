def generate_sales_report(kpis: Dict, anomaly_result: Dict, api_key: str, date_range: str = "") -> str:
    """Generate LLM-powered sales intelligence report."""
    anomaly_summary = "\n".join([
        f"- [{a['severity']}] {a['type']} on {a['date']}: {a['detail']}"
        for a in anomaly_result['anomalies'][:8]
    ])
    insights_text = "\n".join([f"- {i}" for i in anomaly_result['insights']])

    prompt = f"""Generate a professional enterprise sales intelligence report for the following data:

**Period:** {date_range or 'Current Period'}

**KPI Summary:**
- Total Revenue: Rs.{kpis.get('total_revenue', 0):,.0f}
- Average Daily Revenue: Rs.{kpis.get('avg_daily_revenue', 0):,.0f}
- Total Units Sold: {kpis.get('total_units', 0):,}
- Average Order Value: Rs.{kpis.get('avg_order_value', 0):,.2f}
- Target Achievement: {kpis.get('target_achievement', 0):.1f}%
- Return Rate: {kpis.get('return_rate', 0):.2f}%
- Gross Margin: {kpis.get('gross_margin', 0):.1f}%

**Anomalies Detected ({anomaly_result['total_anomalies']} total - {anomaly_result['high_severity']} HIGH, {anomaly_result['medium_severity']} MEDIUM):**
{anomaly_summary if anomaly_summary else 'No anomalies detected.'}

**Data Insights:**
{insights_text}

Write a structured executive report with: Executive Summary, Performance Analysis, Anomaly Assessment, Risk Flags, and 3 Actionable Recommendations. Be specific with numbers."""

    return call_groq(prompt, api_key)


